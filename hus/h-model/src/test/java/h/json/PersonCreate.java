  private static String[] address(String inString)
  {
    if (inString.length() < 5)
    {
      return null;
    }

    String[] ret = inString.split("\\|");
    for (int i = 0; i < ret.length; i++)
    {
      String value = ret[i];
      if ("null".equals(value))
      {
        ret[i] = null;
      }
    }
    return ret;
  }

  private static Roles role(String inString)
  {
    Roles ret = new Roles();
    ret.normalize();
    if (inString.length() > 5)
    {
      for (String value : inString.split("\\|"))
      {
        Role role = EnumUtil.valueOf(value, Role.values());
        if (role != null)
        {
          ret.add(role);
        }
      }
    }

    return ret;
  }

  private static Date newDate(String inText)
  {
    if (inText == null || "1900-1-0".equals(inText) || inText.length() < 5)
    {
      return null;
    }
    Date ret = null;
    try
    {
      ret = new SimpleDateFormat("yyyy-MM-dd").parse(inText);
    }
    catch (ParseException e)
    {
      e.printStackTrace();
    }
    return ret;
  }
